package org.atheNA.day04.control.loop.exercise;

public class Exercise_ForDouble4 {

	public static void main(String[] args) {

		for (int j = 1; j <= 9; j++) {
			for (int i = 2; i <= 9; i++) {
				System.out.print(i + "*" + j + "=" + (i * j) + "	");
			}
			System.out.println("");
		}
		System.out.println("");
	}

}
